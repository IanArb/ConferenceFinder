package com.ianarbuckle.conferencesfinder.ui.conferencedetail.di

import android.content.Context
import com.ianarbuckle.conferencesfinder.ui.conferencedetail.view.ConferenceDetailView
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.android.components.ActivityComponent
import dagger.hilt.android.qualifiers.ActivityContext

@Module
@InstallIn(ActivityComponent::class)
class ConferenceDetailModule {

    @Provides
    fun provideConferenceDetailView(@ActivityContext context: Context): ConferenceDetailView =
        ConferenceDetailView(context)
}